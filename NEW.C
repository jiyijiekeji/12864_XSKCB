void WRITE_16(uchar plat,uchar column,uchar p)
{
	uchar page,page1,col,col_h,col_l;
	uchar i,a,j=0;
	page1= plat*2;
	page=page1+0xb0;
	col=column*16;
	col_h=((col>>4)|0xf0)&0x1f;
	col_l=col&0x0f;
	for(a = 0; a < 2; a++)
	{
		WriteCmd(col_h);
		WriteCmd(col_l);
		WriteCmd(page+a);
		for(i = 0;  i < 16; i ++ )
	 	{
			WriteData(table[p][j++]);	
		}
	}	 
}
